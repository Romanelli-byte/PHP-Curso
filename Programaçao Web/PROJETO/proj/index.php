Quase todo mundo que já usou um processador de texto acidentalmente pressionou a tecla Insert e sobrescreveu tudo acreditando que estava editando. Este artigo descreve um jeito simples para desabilitar a tecla Insert em seu teclado.

Sempre que você pressiona uma tecla, é gerada uma mensagem do Windows que contém um código de chave que a identifica. Programas como o Microsoft Word procuram por mensagens keypress (associadas ao toque de alguma tecla) e tomam ações baseadas no código da chave presente na mensagem. Ao associar que a digitação da tecla Insert terá um resultado nulo, o Windows enviará uma mensagem contendo um valor vazio ao código de chave quando ela for pressionada. Portanto, os programas que estiverem recebendo a mensagem, não executarão a ação associada a Insert, livrando-o da preocupação de sobrescrever as coisas novamente.

